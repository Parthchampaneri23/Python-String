# Write a Python program to remove a newline in Python String = "\nBest \nDeeptech \nPython \nTraining\n"

# Define the input string
input_string = "\nBest \nDeeptech \nPython \nTraining\n"

# Remove newlines using the replace() method
output_string = input_string.replace("\n", "")

# Print the output string
print(output_string)
